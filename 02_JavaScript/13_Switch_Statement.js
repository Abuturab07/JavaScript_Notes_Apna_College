// Used when we have some fixed values that we need to compare to.
let color = "red"

switch(color) {
    case "red" :
        console.log("stop");
        break;
    case "yellow" :
        console.log("slow down");
        break;
    case "green" :
        console.log("G0");
        break;
    default :
        console. log("Broken Light");
}