// Array Methods
// sort: sorts an array
// sorts in ascending order
// doesnt work in numbers becaues it converts them into string

let days = ["monday", "sunday", "wednesday", "tuesday"];

days.sort();  //  ['monday', 'sunday', 'tuesday', 'wednesday']

let squares = [25, 16, 4, 49, 36, 9];

squares.sort();  //  [16, 25, 36, 4, 49, 9]